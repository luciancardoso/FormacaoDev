# Lista é uma estrutura indexada
# Índices  0  1  2  3
numeros = [1, 2, 3, 4]
print(numeros)

frutas = ["Maça", "Banana", "Laranja"]
print(frutas)

misto = [1, True, 3.14, "João", [1, 2, 3, 4, 5]]
print(misto)

matriz = [[1,2,3], [4,5,6], [7,8,9]]
print(matriz)

# Acessar os elementos pelo Índice
print(numeros[3])
print(frutas[2])
print(frutas[-3])
print(misto[1])